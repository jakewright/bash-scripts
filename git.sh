# General git helpers

alias ga="git add ."
alias gb="git branch"
alias gc="git commit"
alias gd="git diff"
alias gl="git log"
alias gp="git push -u"

# The --force-with-lease option is like --force
# but will not overwrite other people's changes.
alias gpf="git push --force-with-lease"

alias gs="git status"
alias rebasem="git fetch --all; git rebase origin/master"
